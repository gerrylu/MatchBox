	public static String GetMimeTypes(String sFileName) {
		String sDefaultMimeType = "text/plain";
		int iExtPos = sFileName.lastIndexOf(".");
		if (iExtPos <= 0) {
			return sDefaultMimeType;
		}
		String sExt = sFileName.substring(iExtPos + 1);
		for (int i = 0; i < mimeTypesList.length; ++i) {
			if (sExt.equalsIgnoreCase(mimeTypesList[i].extention)) {
				return mimeTypesList[i].mType;
			}
		}

		return sDefaultMimeType;
	};
